// Require mongoose
const mongoose = require('mongoose');

// Define the schema
const employeeSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  isAdmin: {
    type: Boolean,
    default: false,
  },
});

const Employee = mongoose.model('Employee', employeeSchema);

// Export the model
module.exports = Employee;